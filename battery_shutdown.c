#include <sys/types.h>
#include <sys/stat.h>
#include <sys/wait.h>
#include <sys/ioctl.h>
#include <sys/time.h>
#include <linux/rtc.h>
#include <linux/types.h>
#include <fcntl.h>
#include <unistd.h>
#include <stdlib.h>
#include <stdio.h>
#include <string.h>
#include <errno.h>
#include <time.h>
#include "battery_monitor.h"

int main(int argc, char *argv[])
{
	int rc;
	int daemon_pid;

	if (!directory_exists(SYS_BMS))
		return 0;

	rc = read_int_from_file(PERSISTENT_LOCATION, PID_FILENAME , &daemon_pid);
	if (rc == 0) {
		/* ask the daemon to store the values */
		kill(daemon_pid, SIGUSR1);
	}
	return 0;
}

